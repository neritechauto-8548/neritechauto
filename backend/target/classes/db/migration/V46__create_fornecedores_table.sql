CREATE TABLE fornecedores (
    id BIGSERIAL PRIMARY KEY,
    empresa_id BIGINT NOT NULL,
    tipo_pessoa VARCHAR(10),
    nome_fantasia VARCHAR(255) NOT NULL,
    razao_social VARCHAR(255),
    cpf VARCHAR(14),
    cnpj VARCHAR(18),
    inscricao_estadual VARCHAR(20),
    inscricao_municipal VARCHAR(20),
    email_principal VARCHAR(255),
    telefone_principal VARCHAR(20),
    celular_principal VARCHAR(20),
    website VARCHAR(255),
    nome_contato VARCHAR(255),
    cargo_contato VARCHAR(100),
    email_contato VARCHAR(255),
    telefone_contato VARCHAR(20),
    endereco_completo TEXT,
    cep VARCHAR(9),
    cidade VARCHAR(100),
    estado CHAR(2),
    prazo_pagamento_dias INT DEFAULT 30,
    limite_credito DECIMAL(12,2),
    desconto_padrao DECIMAL(5,2) DEFAULT 0,
    condicoes_especiais TEXT,
    banco_nome VARCHAR(100),
    banco_agencia VARCHAR(10),
    banco_conta VARCHAR(20),
    banco_pix VARCHAR(255),
    classificacao VARCHAR(1),
    observacoes TEXT,
    ativo BOOLEAN DEFAULT TRUE,
    data_cadastro TIMESTAMP DEFAULT CURRENT_TIMESTAMP,
    data_atualizacao TIMESTAMP DEFAULT CURRENT_TIMESTAMP,
    criado_por BIGINT,
    atualizado_por BIGINT,
    versao INT DEFAULT 0,
    CONSTRAINT fk_fornecedor_empresa FOREIGN KEY (empresa_id) REFERENCES empresa(id)
);
