CREATE TABLE veiculos (
    id BIGSERIAL PRIMARY KEY,
    id_empresa BIGINT,
    cliente_id BIGINT NOT NULL,
    marca_id BIGINT,
    modelo_id BIGINT,
    ano_modelo_id BIGINT,
    placa VARCHAR(10) NOT NULL,
    renavam VARCHAR(11),
    chassi VARCHAR(17),
    numero_motor VARCHAR(50),
    cor_externa VARCHAR(50),
    quilometragem_atual INT DEFAULT 0,
    quilometragem_cadastro INT DEFAULT 0,
    data_ultima_revisao DATE,
    proxima_revisao_km INT,
    proxima_revisao_data DATE,
    status VARCHAR(20),
    observacoes TEXT,
    valor_estimado DECIMAL(12,2),
    data_valor_estimado DATE,
    foto_principal_url VARCHAR(500),
    data_cadastro TIMESTAMP DEFAULT CURRENT_TIMESTAMP,
    data_atualizacao TIMESTAMP DEFAULT CURRENT_TIMESTAMP,
    criado_por BIGINT,
    atualizado_por BIGINT,
    versao INT DEFAULT 0,
    CONSTRAINT fk_veiculo_empresa FOREIGN KEY (id_empresa) REFERENCES empresa(id),
    CONSTRAINT fk_veiculo_cliente FOREIGN KEY (cliente_id) REFERENCES cliente(id),
    CONSTRAINT fk_veiculo_marca FOREIGN KEY (marca_id) REFERENCES marcas_veiculos(id),
    CONSTRAINT fk_veiculo_modelo FOREIGN KEY (modelo_id) REFERENCES modelos_veiculos(id),
    CONSTRAINT fk_veiculo_ano_modelo FOREIGN KEY (ano_modelo_id) REFERENCES anos_modelo(id),
    CONSTRAINT uk_veiculo_placa_empresa UNIQUE (placa, id_empresa)
);
